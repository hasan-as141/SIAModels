
public class Aritmatika {

    protected double a;
    protected double b;

    protected double add(double a, double b) {
        return a + b;
    }

    protected double subtract(double a, double b) {
        return a - b;
    }

    protected double times(double a, double b) {
        return a * b;
    }

    protected double divide(double a, double b) {
        return a / b;
    }
}
